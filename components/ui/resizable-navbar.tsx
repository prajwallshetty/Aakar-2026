export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
  onClick?: () => void;
  [key: string]: unknown;
}) => {
  const baseStyles =
    "px-4 py-2 rounded-md text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const variantStyles = {
    primary:
      "bg-gradient-to-r from-[#6344F5] to-[#AE48FF] text-white shadow-[0_0_20px_rgba(99,68,245,0.3),_0_1px_0_rgba(255,255,255,0.1)_inset]",
    secondary: "bg-transparent text-neutral-300 shadow-none hover:text-white",
    dark: "bg-[#0d0526] text-white border border-[#6344F5]/30 shadow-[0_0_20px_rgba(99,68,245,0.15)]",
    gradient:
      "bg-gradient-to-r from-[#6344F5] to-[#AE48FF] text-white shadow-[0px_2px_0px_0px_rgba(255,255,255,0.2)_inset]",
  };

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const Component = Tag as any;

  return (
    <Component
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Component>
  );
};

